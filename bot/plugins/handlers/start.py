async def StartHandler(bot,message):
    
    await ReferralData(bot,message)
    user_data(message)
    if is_verified(message.chat.id) is True:
        if is_airdrop_open() is True:
            if is_user_join_airdrop(message.chat.id) is False:
                await Welcome(bot,message)
            else:
                await Start(bot,message)
        else:
            await AirdropEnd(bot,message)
    else:
        caption='Hello **{}** 👋, select all the emojis you see in the picture.\n\n🔺 Only one mistake allowed.'.format(message.chat.first_name)
        emoji=genarate_emoji(message.chat.id)
        markup=InlineKeyboardMarkup([[InlineKeyboardButton(x,callback_data=x) for x in emoji[0]],
                                     [InlineKeyboardButton(x,callback_data=x) for x in emoji[1]],
                                     [InlineKeyboardButton(x,callback_data=x) for x in emoji[2]]])
        msg=await bot.send_photo(message.chat.id,'telegram-airdropbot-bot/bot/plugins/utils/image/testemoji.png',caption=caption,reply_markup=markup,parse_mode='md')



    
